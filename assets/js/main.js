document
  .getElementById("contact-form")
  .addEventListener("submit", function (event) {
    event.preventDefault();

    const loading = document.querySelector(".loading");
    const sentMessage = document.querySelector(".sent-message");
    const errorMessage = document.querySelector(".error-message"); // Assurez-vous d'avoir un div pour les erreurs

    loading.style.display = "flex";
    sentMessage.style.display = "none";
    if (errorMessage) errorMessage.style.display = "none";

    const serviceID = "service_vo2m8sh";
    const templateID = "template_ghibr42";

    // Préparer les paramètres pour le template EmailJS
    const templateParams = {
      name: document.getElementById("name").value,
      email: document.getElementById("email").value,
      subject: document.getElementById("subject").value,
      message: document.getElementById("message").value,
      time: new Date().toLocaleString(),
    };

    // Envoyer le formulaire via EmailJS
    emailjs.send(serviceID, templateID, templateParams).then(
      () => {
        loading.style.display = "none";
        sentMessage.style.display = "block";

        // Afficher le message sur la page
        document.getElementById("display-name").innerText = templateParams.name;
        document.getElementById("display-subject").innerText =
          templateParams.subject;
        document.getElementById("display-text").innerText =
          templateParams.message;
        document.getElementById("display-time").innerText = templateParams.time;
        document.getElementById("display-message").style.display = "block";

        this.reset();
      },
      (err) => {
        loading.style.display = "none";
        if (errorMessage) {
          errorMessage.innerText = "❌ Erreur : " + JSON.stringify(err);
          errorMessage.style.display = "block";
        } else {
          alert("❌ Erreur : " + JSON.stringify(err));
        }
      }
    );
  });